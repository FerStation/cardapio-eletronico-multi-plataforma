package Trasferencia;

public class DtoUsuario {
	
	private String usuarioAtivo;
	private String nvAcesso;
	private String login;
	private String nome;
	private String senha;
	private String rg;
	private String cpf;
	private String email;
	private String telefone;
	private String celular;
	
	
	public String getUsuarioAtivo() {
		return usuarioAtivo;
	}
	public void setUsuarioAtivo(String usuarioAtivo) {
		this.usuarioAtivo = usuarioAtivo;
	}
	public String getNvAcesso() {
		return nvAcesso;
	}
	public void setNvAcesso(String nvAcesso) {
		this.nvAcesso = nvAcesso;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getRg() {
		return rg;
	}
	public void setRg(String rg) {
		this.rg = rg;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	
	

}
